package com.gft.model;

public class Carro {

private String marca;
	
	private int portas;
	
	private int ano;
	
	public String getmarca() {
		return marca;
	}
	
	public void setmarca(String marca) {
		this.marca = marca;
	}
	
	public int getportas() {
		return portas;
	}
	
	public void setportas(int portas) {
		this.portas = portas;
	}
	
	public int getano() {
		return ano;
	}
	
	public void setano(int ano) {
		this.ano = ano;
	}

}
